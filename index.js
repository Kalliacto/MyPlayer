function renderElement(el, data) {
    const palyListElement = document.createElement(`${el}`);

    if (!!data) {
        if (el === 'img' || el === 'audio') {
            palyListElement.src = data;
        } else {
            palyListElement.append(data);
        }
    }

    return palyListElement;
}

function renderTrack(dataTrack) {
    const liElement = renderElement('li');

    liElement.append(renderElement('span', dataTrack.artist), ` - `, renderElement('span', dataTrack.track));
    liElement.append(renderElement('img', dataTrack.posterUrl));

    const audioElement = renderElement('audio', dataTrack.src);
    liElement.append(audioElement);
    audioElement.controls = true;

    return liElement;
}
function renderPlaylistHeader(inputPlaylistForRendering) {
    document.body.appendChild(renderElement('h2', inputPlaylistForRendering.title));
    document.body.append(`Всего треков в плейлисте: `, renderElement('span', inputPlaylistForRendering.tracks.length));
    document.body.appendChild(renderElement('img', inputPlaylistForRendering.coverImageUrl));
}
function renderPlyList(playlistForRendering) {
    renderPlaylistHeader(playlistForRendering);
    let ulElement = renderElement('ul');

    playlistForRendering.tracks.forEach((item) => {
        ulElement.append(renderTrack(item));
    });

    document.body.append(ulElement);
}

renderPlyList(playList);
